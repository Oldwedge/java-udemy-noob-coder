
public class DoWhile {
	
	public static void main(String[] args) {
		
		// Do While loops differ from the others in the sense that they guarantee that the code is executed at least once
		
		int i = 3;
		
		
		do {
			
			System.out.println("Da shit");
			
			i++;
			
		}
		while(i < 5);
		
			
		
		do {
			
			System.out.println("Da next shit");
			
			i++;
			
		}
		while(i < 5);
		
	}

}

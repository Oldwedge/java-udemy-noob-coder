
public class ConditionalOperators {

	public static void main(String[] args) {
		
		int num = 5;
		
		if(num == 5 && num % 2 == 1) {
			
			System.out.println(num + " is equal to 5 and is odd");
			
		}
		if(num == 5 || num % 2 == 0) 
		{
			
			System.out.println(num + " is equal to 5 or is even");
			
			
		}
		
	}
	
}

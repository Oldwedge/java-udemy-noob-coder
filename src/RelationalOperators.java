
public class RelationalOperators {

	public static void main(String[] args) {
		
		int num1 = 5;
		int num2 = 7;
		
		boolean trueOrFalse = num1 < num2;
		
		System.out.println(num1 + " < " + num2 + " = " + trueOrFalse);
		
		trueOrFalse = num1 > num2;
		
		System.out.println(num1 + " > " + num2 + " = " + trueOrFalse);
		
		trueOrFalse = num1 <= num2;
		
		System.out.println(num1 + " <= " + num2 + " = " + trueOrFalse);
		
		trueOrFalse = num1 >= num2;
		
		System.out.println(num1 + " >= " + num2 + " = " + trueOrFalse);
		
		trueOrFalse = num1 == num2;
		
		System.out.println(num1 + " == " + num2 + " = " + trueOrFalse);
		
		trueOrFalse = num1 != num2;
		
		System.out.println(num1 + " != " + num2 + " = " + trueOrFalse);
		
	}
	
}
